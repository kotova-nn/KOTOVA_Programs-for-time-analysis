# ===================================================================================================================
#                                                Импорт библиотек
# ===================================================================================================================

from sqlalchemy import create_engine, Column, Integer, String, DateTime, Boolean, Text
from sqlalchemy.ext.declarative import declarative_base
from sqlalchemy.orm import sessionmaker
from datetime import datetime
import os


# ===================================================================================================================
#                                                Настройка подключения к базе данных
# ===================================================================================================================

# Получаем URL базы данных из переменной окружения (для Render.com) или используем SQLite файл локально
DATABASE_URL = os.environ.get('DATABASE_URL', 'sqlite:///certificates.db')

# Для SQLite требуется дополнительный параметр check_same_thread=False,
# чтобы разрешить доступ к базе данных из разных потоков (Flask использует многопоточность)
if DATABASE_URL.startswith('sqlite'):
    engine = create_engine(DATABASE_URL, connect_args={"check_same_thread": False})
else:
    # Для PostgreSQL (на Render.com) дополнительные параметры не нужны
    engine = create_engine(DATABASE_URL)

# Создаём фабрику сессий для работы с базой данных
SessionLocal = sessionmaker(bind=engine)

# Базовый класс для всех моделей SQLAlchemy
Base = declarative_base()


# ===================================================================================================================
#                                                Модель таблицы share_tokens
# ===================================================================================================================

class ShareToken(Base):
    """
    Модель таблицы share_tokens для хранения информации о сгенерированных ссылках.
    
    Каждая запись соответствует одной уникальной ссылке на сертификат.
    При создании ссылки все данные ученика фиксируются и сохраняются в JSON-формате,
    чтобы при последующих открытиях ссылки отчёт не менялся, даже если датасет обновился.
    """
    __tablename__ = "share_tokens"

    # ===============================================================================================================
    #                                                Основные поля
    # ===============================================================================================================
    
    id = Column(Integer, primary_key=True, index=True)
    """Первичный ключ, автоматически увеличивается"""
    
    token = Column(String(36), unique=True, index=True, nullable=False)
    """
    Уникальный UUID-токен, по которому можно открыть сертификат.
    Длина 36 символов соответствует стандартному формату UUID (xxxxxxxx-xxxx-xxxx-xxxx-xxxxxxxxxxxx).
    Индексирован для быстрого поиска.
    """
    
    student_name = Column(String(200))
    """Имя и фамилия ученика (для отображения на странице сертификата)"""
    
    student_data_json = Column(Text)
    """
    JSON-строка с полными данными ученика на момент создания ссылки.
    Содержит все метрики: решённые задачи, баллы, минуты, дни активности,
    любимый день недели, тип активности и т.д.
    Формат: словарь Python, преобразованный в JSON.
    """
    
    
    # ===============================================================================================================
    #                                                Поля периода обучения
    # ===============================================================================================================
    
    period_type = Column(String(50))
    """Тип периода: 'year' (год) или 'trimester' (триместр)"""
    
    period_number = Column(Integer)
    """Номер периода: 1 для года, 1, 2 или 3 для триместра"""
    
    period_year = Column(Integer)
    """Учебный год (например, 2026 для периода 01.09.2025 — 31.05.2026)"""
    
    
    # ===============================================================================================================
    #                                                Поля для определения типа отчёта
    # ===============================================================================================================
    
    age_group = Column(String(20))
    """Возрастная группа: 'primary' (начальная школа, 1-4 класс) или 'senior' (старшая школа, 5-11 класс)"""
    
    grade = Column(Integer)
    """Класс ученика (1-11). Используется для выбора шаблона и расчёта локации для начальной школы"""
    
    
    # ===============================================================================================================
    #                                                Поля управления ссылкой
    # ===============================================================================================================
    
    is_active = Column(Boolean, default=True)
    """
    Статус активности ссылки.
    True — ссылка активна, можно открыть сертификат.
    False — ссылка отозвана администратором, при попытке открытия возвращается ошибка 403.
    """
    
    created_at = Column(DateTime, default=datetime.utcnow)
    """Дата и время создания ссылки (в формате UTC)"""
    
    views_count = Column(Integer, default=0)
    """Счётчик просмотров — сколько раз открывали эту ссылку. Увеличивается при каждом запросе к /share/<token>"""
    
    
    # ===============================================================================================================
    #                                                Методы модели
    # ===============================================================================================================
    
    def is_valid(self):
        """
        Проверяет, активна ли ссылка.
        
        Returns:
            tuple: (is_valid, message)
                - is_valid (bool): True если ссылка активна, False если отозвана
                - message (str): "OK" или "Ссылка отозвана"
        """
        if not self.is_active:
            return False, "Ссылка отозвана"
        return True, "OK"
    
    def increment_views(self):
        """Увеличивает счётчик просмотров на 1. Вызывается при каждом открытии страницы сертификата."""
        self.views_count += 1


# ===================================================================================================================
#                                                Создание таблиц
# ===================================================================================================================

# Создаёт все таблицы в базе данных, определённые в моделях (если они ещё не существуют)
Base.metadata.create_all(bind=engine)


# ===================================================================================================================
#                                                Функция для получения сессии БД
# ===================================================================================================================

def get_db():
    """
    Генератор для получения сессии базы данных.
    Используется в FastAPI/Flask как зависимость для автоматического закрытия соединения.
    
    Пример использования в эндпоинте:
        db = next(get_db())
        try:
            token = db.query(ShareToken).filter(...).first()
        finally:
            db.close()
    
    Yields:
        Session: сессия SQLAlchemy для работы с базой данных
    """
    db = SessionLocal()
    try:
        yield db
    finally:
        db.close()